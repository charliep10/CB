SELECT
       CAST (CASE WHEN CALL_DATE = '2018-07-22' THEN '2018-07-23'
		WHEN CALL_DATE = '2018-07-28' THEN '2018-07-27'
		WHEN CALL_DATE = '2018-07-29' THEN '2018-07-27'
		WHEN CALL_DATE = '2018-08-04' THEN '2018-08-03'
		WHEN CALL_DATE = '2018-08-05' THEN '2018-08-03'
		WHEN CALL_DATE = '2018-08-11' THEN '2018-08-10'
		WHEN CALL_DATE = '2018-08-12' THEN '2018-08-10'
		WHEN CALL_DATE = '2018-08-18' THEN '2018-08-17'
		WHEN CALL_DATE = '2018-08-19' THEN '2018-08-17'
		ELSE CALL_DATE END AS DATE) AS CALL_DATE --UPDATE EVERY MONTH
       ,CASE WHEN REGION = 'CALIFORNIA' THEN 'California'
			WHEN REGION = 'HOUSTON' THEN 'Houston'
			WHEN REGION = 'MOUNTAIN' THEN 'Mountain West'
			WHEN REGION = 'MILE HIGH' THEN 'Mountain West'
			WHEN REGION = 'PORTLAND' THEN 'Portland'
			WHEN REGION = 'SEATTLE' THEN 'Seattle'
			WHEN REGION = 'TWIN CITIES' THEN 'Twin Cities'
			ELSE 'Other' END AS REGION			
       ,'Commercial HSD' AS PRODUCT
       ,ANSLOGIN
       ,CALLTYPE_ID
       ,SUM(NCH) AS CALL_CNT
       ,SUM(TRANSFERRED) AS TRANSFER_CNT
       ,SUM(XFER_IN) AS XFER_IN
FROM
       [RptCare].[Ext].[vWCP_v_CMS_ECH_DETAIL] AS a (nolock)
WHERE
       CALL_DATE BETWEEN '2018-07-22' AND '2018-08-22' --UPDATE EVERY MONTH
       AND DISPSPLIT IN (500, 5500, 5501, 5502)
       AND NCH = 1
    AND DISPOSITION = 2 --HANDLED
GROUP BY
       CALL_DATE
       ,REGION
       --,PRODUCT
       ,ANSLOGIN
       ,CALLTYPE_ID
ORDER BY
       CALL_DATE,
       a.ANSLOGIN
